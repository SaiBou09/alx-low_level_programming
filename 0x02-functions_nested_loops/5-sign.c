#include "main.h"

/**
 * print_sign  - prints sign of a number
 * @n: takes input from function
 *
 * Return: 1 if n>0
 *         0 if n=0
 *         -1 if n<0
 */

int print_sign(int n)
{
if (n > 0)
{
_putchar(43);
return (1);
}
if (n = 0)
{
_putchar(0);
return (0);
}
if (n < 0)
{
_putchar(45);
return (-1);
}
}
